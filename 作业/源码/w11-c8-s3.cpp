void triangle(int n)
{
	int digit[13][13] = { 0 };
	for (int i = 0;i < 13;i++) {
		digit[i][0] = 1;
		for (int j = 1; j <= i; j++) {
			digit[i][j] = digit[i - 1][j - 1] + digit[i - 1][j];

		}
	}

	for (int i = 0; i < n; i++) {
		for (int k = 0; k < n - i - 1; k++) {
			cout << setw(6) << " ";
		}
		for (int j = 0; j <= i; j++) {
			cout << setw(6) << digit[i][j] ;
		}
		cout << endl;
	}



}



















